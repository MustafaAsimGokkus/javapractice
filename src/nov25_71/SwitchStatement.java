package nov25_71;

public class SwitchStatement {
public static void main(String[] args) {
	String gender = "girl";
	
	switch (gender) {
	case "female":
		System.out.println("This is a girl");
		break;
	
	case "male":
    System.out.println("This is a boy");
    break;
    
    default:
    	System.out.println("Enter a valid gender");
    
    
    
    
     }
  }
} 