package nov25_71;
   /*
   Write a program to add counting numbers from 23 to 57 by using for-loop.
   Print the sum on the console
   */
  public class ForLoop9004 {
	 
	  public static void main(String[] args) {
	
		  int sum = 0;
	   
	   for (int i = 23; i<= 58; i++) {
			sum+=i;
		}
       
	 System.out.println(sum);
	 
  }
}
